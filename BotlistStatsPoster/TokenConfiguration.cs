namespace BotlistStatsPoster
{
    public class TokenConfiguration
    {
        /// <summary> Your bot's <see href="https://top.gg/">top.gg</see> token </summary>
        public string Topgg { get; set; }

        /// <summary> Your <see href="https://discord.bots.gg/">discord.bots.gg</see> token </summary>
        public string DiscordBots { get; set; }

        /// <summary> Your bot's <see href="https://botsfordiscord.com/">botsfordiscord.com</see> token </summary>
        public string BotsForDiscord { get; set; }

        /// <summary> A <see href="https://bots.ondiscord.xyz/">bots.ondiscord.xyz</see> token which includes your bot </summary>
        public string BotsOnDiscord { get; set; }

        /// <summary> Your <see href="https://discord.boats/">discord.boats</see> token </summary>
        public string DiscordBoats { get; set; }

        /// <summary> Your bot's <see href="https://discordbotlist.com/">discordbotlist.com</see> token </summary>
        public string DiscordBotList { get; set; }

        /// <summary> Your bot's <see href="https://botlist.space/">botlist.space</see> token </summary>
        public string BotlistSpace { get; set; }

        /// <summary> Your bot's <see href="https://discordextremelist.xyz/">discordextremelist.xyz</see> token </summary>
        public string DiscordExtremeList { get; set; }
    }
}
